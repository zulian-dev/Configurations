local java = {}



--------------------------------------------------------------------------------
-- Mason -----------------------------------------------------------------------
--------------------------------------------------------------------------------

java.mason = {
  "jdtls",
}



--------------------------------------------------------------------------------
-- LSP -------------------------------------------------------------------------
--------------------------------------------------------------------------------

java.lsp = function(lspconfig, capabilities, on_attach)
  lspconfig.jdtls.setup({
    capabilities = capabilities,
    on_attach = on_attach,
    filetypes = { "java" },
  })
end



return java