local rust = {}

--------------------------------------------------------------------------------
--- Filetypes -----------------------------------------------------------------
--------------------------------------------------------------------------------

rust.filetypes = {
  "rust",
  "ron"
}

--------------------------------------------------------------------------------
-- Mason -----------------------------------------------------------------------
--------------------------------------------------------------------------------

rust.mason = {
  "rust_analyzer",
}

--------------------------------------------------------------------------------
-- LSP -------------------------------------------------------------------------
--------------------------------------------------------------------------------

rust.lsp = function(lspconfig, capabilities, on_attach)
  lspconfig.rust_analyzer.setup({
    capabilities = capabilities,
    on_attach = on_attach,
    filetypes = rust.filetypes,
  })
end

return rust
